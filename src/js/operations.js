var prettyName = (n)=>{
  n = n.replace(/json/g, 'JSON')
  return ( n.charAt(0).toUpperCase() + n.substring(1) ).replace(/_/g, ' ')
}

var DEBUG = false

var listTypes = []
for(var i in operations){
  var op = operations[i]
  op.name = prettyName(i)
  op.operation = i
  if(!op.optional) op.optional = []
  for(var j in op.params){
    var param = op.params[j]
    param.name = prettyName(j)
    param.value = ''
    param.use_it = true
    switch(param.type){
      case 'asset':
      case 'account':
      case 'string':
      case 'public_key':
      case 'number':
      case 'time':
      case 'float-string':
        param.typeUI = 'text'
        break
      case 'textarea':
      case 'buffer':
      case 'json':
      case 'object':
        param.typeUI = 'textarea'
        break
      case 'boolean':
        param.typeUI = 'checkbox'
        break
      default:
        throw new Error(`No typeUI defined for the type '${param.type}'`)
    }
    if(DEBUG){
      var type = listTypes.find( (t)=>{return t === param.type} )
      if(!type) listTypes.push(param.type)
    }
  }
}

if(DEBUG) console.log(listTypes)

